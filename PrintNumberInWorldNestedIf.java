package PartiallyOpenedLabBooks;

public class PrintNumberInWorldNestedIf {

	public static void main(String[] args) {
		int number=5;
		if(number==1) {
			System.out.println("1");
		}
		else if(number==2){
			System.out.println("2");
		}
		else if(number==3) {
			System.out.println("3");
		}
		else if(number==4) {
			System.out.println("4");
		}
		else if(number==5) {
			System.out.println("5");
		}
		else {
			System.out.println("Done");
		}

	}

}
